<h3>Inloggen</h3>
Is je instelling aangesloten op {cfg:site_name}? Druk op {cfg:logon} en kies je instelling uit de lijst van instellingen. 
Staat je instelling niet in de lijst, of kun je niet inloggen? Neem dan contact op met de helpdesk van je instelling.
Ben je ingelogd? Je kunt nu:
<ul class="fa-ul">
    <li><i class="fa-li fa fa-caret-right"></i>1 of meer bestanden (versleuteld) uploaden.</li> 
    <li><i class="fa-li fa fa-caret-right"></i>een bericht ontvangen als je upload klaar is.</li>
    <li><i class="fa-li fa fa-caret-right"></i>een downloadlink krijgen.</li>
    <li><i class="fa-li fa fa-caret-right"></i>gastgebruikers uitnodigen.</li>
</ul>


<h3>Gastgebruikers</h3>
<p>Heb je een uitnodigingslink gekregen, terwijl je die niet verwachtte? 
	Controleer voor je aan de slag gaat of alle koppelingen hierin daadwerkelijk naar {cfg:site_name} verwijzen. 
	De uitnodiging is mogelijk niet legitiem.
	</p>
Verwachtte je wel een uitnodiging? Upload nu eenvoudig bestanden met de informatie in de uitnodigingse-mail en deel de downloadlink. 
	<p>
Geen downloadlink gekregen? Geef dan e-mailadressen op van degenen die je wil laten downloaden.
	</p>

